package com.syntax.class02;

public class WaysToCreateVariable {

	public static void main(String[] args) {
		
		//To create a variable we need 2 things: datatype&name
		
		/*
		 *First way to create a variable:
		 *datatype name = value
		 */
		int temperature=60;
		
		/*
		 *Second way to create a variable:
		 *Create a variable (datatype and name)
		 *Then, assign a value to that variable
		 */
		int sum; //creating variable
		sum=80;  //assigning value
		sum=50;  //replacing assigned value with a new value
		
		//creating multiple variables at once:
		int num1, num2, num3;
		num1=10;
		num2=20;
		num3=30;
		
		
		
	}

}
