package com.syntax.class01;

public class DataTypes {

	public static void main(String[] args) {

		// To create a variable, we need: datatype and name

		// Whole numbers
		byte box1 = 21; // Here, datatype is byte, name is box1, value is 21. for byte datatype min.
						// value=-128, max value=127
		short box2 = 2021; // for short datatype min. value=-32768, max value=327677
		int box3 = 76666;
		long box4 = 667773737898939239L; // put a letter "L" in case of very long number

		// Decimals
		float container1 = 1.99F; // put a letter "F" to specify it is a float number
		double container2 = 2.99;

		// To represent just 1 character
		char gender = 'm';
		char dollar = '$';

		// To represent true or false (yes or no)
		boolean hungry = true;
		boolean sleepy = false;

		// How to access values from variables?
		System.out.println(container2); // do not use "" to access variables

		// access and print values from all variables

	}

}
